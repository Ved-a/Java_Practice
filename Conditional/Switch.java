package Conditional;

public class Switch {
    public static void main(String[] args) {
        int n = 33;
        switch(n){
            case 1:
            System.out.println("Have Breakfast");
            break;
            case 2:
            System.out.println("Have Lunch");
            break;
            case 3:
            System.out.println("Have Dinner");
            break;
            default:
            System.out.println("Go and sleep");
        }
    }
}
